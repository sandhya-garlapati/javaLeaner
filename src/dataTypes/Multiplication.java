package dataTypes;

public class Multiplication {

    public static void main (String[] args){

        int m1 = 765;
        int m2 = 87654;
        int mul = m1*m2;
        System.out.println(mul);

        int m3 = 5;
        float m4 = 87.5f;
        float mul2 = m4*m3;
        System.out.println(mul2);

        double m5 = 5.5;
        double m6 = 8.768;
        double mul3 = m5*m6;
        System.out.println(mul3);

    }
}

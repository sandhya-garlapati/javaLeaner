package dataTypes;

public class Subtraction {

    public static void main(String[] args){

        int s1 = 5;
        int s2 = 456;
        int sub = s1-s2;
        System.out.println(sub);

        float s3 = 5.5f;
        float s4 = 12.6f;
        float sub1 = s4-s3;
        System.out.println(sub1);

        double s5 = 6.55;
        double s6 = 5.3;
        double sub2 = s5-s6;
        System.out.println(sub2);


    }


}


